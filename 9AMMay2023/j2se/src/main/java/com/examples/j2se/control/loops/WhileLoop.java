package com.examples.j2se.control.loops;

public class WhileLoop {
	public static void main(String[] args) {
		int i=0;	//init
		while(i<5) {	//while(con)
			System.out.println(i+1+". While Loop");
			i++;		//iter
		}
		System.out.println("END");
	}
}
/*
syntax: 
init;
while(con){
	statements to be repeated;
	iteration;
	}


*/