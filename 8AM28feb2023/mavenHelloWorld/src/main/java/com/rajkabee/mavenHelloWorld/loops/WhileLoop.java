package com.rajkabee.mavenHelloWorld.loops;

public class WhileLoop {
	public static void main(String[] args) {
		int i=0;		//init
		while(i<5) {	//while(condition)
			i++;		//iteration
			System.out.println(i+". While Loop");
		}
	}
	/*
	 * Syntax
	 * initialization
	 * while(condition){
	 * 		work to be done
	 * 		iteration
	 * }
	 */
}
