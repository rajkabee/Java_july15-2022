package com.examples.j2se.control.loops;


public class ForLoop {
	public static void main(String[] args) {
		int i;
		//for(init; con; iter){}
		for(i=0 ;i<5; i++) {
			System.out.println(i+1+". For Loop");
		}
		System.out.println("END");
	}
}
